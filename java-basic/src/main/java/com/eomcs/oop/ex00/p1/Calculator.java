package com.eomcs.oop.ex00.p1;

public class Calculator {

  // Calculator 클래스로
  // plus ~ multiple 을 옮기고
  // main에서 Calculator. 추가해주기

  // p2 로 넘어가기
  // static int result; 추가
  // plus(int result) 에서 result 빼주기
  // int 를 void로 바꾸기

  // p2 CalculatorTest 로 이동

  static int plus(int result, int value) {
    return result + value;
  }

  static int minus(int result, int value) {
    return result - value;
  }

  static int multiple(int result, int value) {
    return result * value;
  }
}
